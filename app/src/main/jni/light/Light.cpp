#include "Light.h"


void Light::setAmbientComponent(float amb) {
    ambient = amb;
}

float Light::getAmbientComponent() {
    return ambient;
}

void Light::setX(float x) {
    xPos = x;
}

float Light::getX() {
    return xPos;
}

void Light::setY(float y) {
    yPos = y;
}

float Light::getY() {
    return yPos;
}

void Light::setZ(float z) {
    zPos = z;
}

float Light::getZ() {
    return zPos;
}

void Light::setSpecularExp(float exp) {
    specularExp = exp;
}

float Light::getSpecularExp() {
    return specularExp;
}



















